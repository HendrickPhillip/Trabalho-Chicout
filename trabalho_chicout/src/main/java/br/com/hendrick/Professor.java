package br.com.hendrick;

public class Professor {
    private String nome_prof;
    private String rg_prof;
    private String cpf_prof;
    private String sexo;
    private String telefone;
    private int dia_prof;
    private int mes_prof;
    private int ano_prof;

    public String getNome_prof() {
        return nome_prof;
    }

    public void setNome_prof(String nome_prof) {
        this.nome_prof = nome_prof;
    }

    public String getRg_prof() {
        return rg_prof;
    }

    public void setRg_prof(String rg_prof) {
        this.rg_prof = rg_prof;
    }

    public String getCpf_prof() {
        return cpf_prof;
    }

    public void setCpf_prof(String cpf_prof) {
        this.cpf_prof = cpf_prof;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public int getDia_prof() {
        return dia_prof;
    }

    public void setDia_prof(int dia_prof) {
        this.dia_prof = dia_prof;
    }

    public int getMes_prof() {
        return mes_prof;
    }

    public void setMes_prof(int mes_prof) {
        this.mes_prof = mes_prof;
    }

    public int getAno_prof() {
        return ano_prof;
    }

    public void setAno_prof(int ano_prof) {
        this.ano_prof = ano_prof;
    }

}
